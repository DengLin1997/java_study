package dayjava;

public class Biaodaoshi {
	public static void main(String[] args) {
		//表达式
		int i=3+2;
		byte a=127;
		byte b=1;
		
		int c=a+b;
		/*
		 * 浮点类型的常量 默认也是double
		 * 3.14 类型位double 
		 * a 类型位byte
		 */
		double d=a+3.14;
		/*
		 * 整数类型的常量 默认也是int
		 * 1600 类型是int 
		 * a 类型是byte
		 * 运算的结果 int 
		 */
		int s=a+1600;
		
		float f=3.14f;
		long l=1000l;
	}

}
